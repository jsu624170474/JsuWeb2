package com.yqjr.entity;

public class GlobalConfigsDO {
	private Integer gcId;
	private String gcKey;
	private String gcValue;
	private String gcRmk;
	
	
	public Integer getGcId() {
		return gcId;
	}
	public void setGcId(Integer gcId) {
		this.gcId = gcId;
	}
	public String getGcKey() {
		return gcKey;
	}
	public void setGcKey(String gcKey) {
		this.gcKey = gcKey;
	}
	public String getGcValue() {
		return gcValue;
	}
	public void setGcValue(String gcValue) {
		this.gcValue = gcValue;
	}
	public String getGcRmk() {
		return gcRmk;
	}
	public void setGcRmk(String gcRmk) {
		this.gcRmk = gcRmk;
	}
}
